import React from 'react';
import { Download } from 'lucide-react';
import { useEvaluationStore } from '@/lib/stores/evaluation.store';

interface Props {
  jobId: string;
  evaluationId?: string;
  panelName?: string;
  candidateName?: string;
  score?: number;
  categories?: Record<string, number> | null;
}

export function ExportButton({
  jobId,
  evaluationId,
  panelName,
  candidateName,
  score,
  categories,
}: Props) {
  const store = useEvaluationStore();
  const effectiveScore = score ?? store.panelScore;
  const effectiveCategories = (categories ?? store.dimensions) as Record<string, number> | null;

  function downloadPDF() {
    const rows = Object.entries(effectiveCategories ?? {})
      .map(
        ([k, v]) =>
          `<tr><td style="padding:6px 12px">${k}</td><td style="padding:6px 12px;text-align:right">${Number(v).toFixed(2)}</td></tr>`
      )
      .join('');

    const html = `
      <html>
        <head>
          <title>Panel Evaluation — ${jobId}</title>
          <style>
            body { font-family: sans-serif; padding: 32px; color: #111; }
            h1   { font-size: 20px; margin-bottom: 8px; }
            h2   { font-size: 15px; margin-top: 24px; }
            table { border-collapse: collapse; width: 100%; margin-top: 8px; }
            th, td { border: 1px solid #ddd; padding: 6px 12px; }
            th { background: #f5f5f5; text-align: left; }
            .meta { color: #555; font-size: 14px; margin: 4px 0; }
            .footer { color: #aaa; font-size: 11px; margin-top: 32px; }
          </style>
        </head>
        <body>
          <h1>Panel Evaluation Report</h1>
          <p class="meta"><strong>Job ID:</strong> ${jobId}</p>
          ${panelName ? `<p class="meta"><strong>Panel:</strong> ${panelName}</p>` : ''}
          ${candidateName ? `<p class="meta"><strong>Candidate:</strong> ${candidateName}</p>` : ''}
          <p class="meta"><strong>Panel Score:</strong> ${Number(effectiveScore ?? 0).toFixed(1)} / 11.0</p>
          <h2>Dimension Breakdown</h2>
          <table>
            <thead><tr><th>Dimension</th><th>Score</th></tr></thead>
            <tbody>${rows}</tbody>
          </table>
          <p class="footer">Generated by Panel Pulse AI</p>
        </body>
      </html>
    `;

    const w = window.open('', '_blank');
    if (w) {
      w.document.write(html);
      w.document.close();
      w.print();
    }
  }

  return (
    <button
      type="button"
      onClick={downloadPDF}
      className="inline-flex items-center gap-2 px-4 py-2 bg-orange-500/15 border border-orange-500/30 text-orange-300 rounded-lg hover:bg-orange-500/25 transition-colors text-sm font-medium"
    >
      <Download className="w-4 h-4" />
      Export PDF
    </button>
  );
}
